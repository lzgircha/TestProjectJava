public class OperatorDemo_3 {
    public static void main(String[] args) {
        int number=10;
        System.out.println(number/3);//will get the whole number answer which is 3
        System.out.println(number%3);//will show the remainder of the decision 10/3 , R=1 (MODULUS operator)
    }
}
